    fun randomWord(
        language: AppLanguage,
        category: HangmanCategory,
        previousAnswer: String? = null,
        recentAnswers: Collection<String> = emptyList(),
    ): PuzzleWord {
        val wordMap = when (language) {
            AppLanguage.English -> englishWords
            AppLanguage.Polish -> polishWords
            AppLanguage.Russian -> russianWords
        }
        val candidates = wordMap.getValue(category)
        val recent = recentAnswers.map(GameEngine::normalizeAnswer).toSet()
        val filtered = candidates
            .filterNot { it.answer == previousAnswer }
            .filterNot { it.answer in recent }
            .ifEmpty {
                candidates.filterNot { it.answer == previousAnswer }.ifEmpty { candidates }
            }
        val selection = filtered.random(Random.Default)
        return selection.copy(answer = GameEngine.normalizeAnswer(selection.answer))
    }
